let add = (num1, num2) => {
    return num1 + num2;
};
let sub = (num1, num2) => {
    return num1 - num2;
};
let multip = (num1, num2) => {
    return num1 * num2;
};
let divide = (num1, num2) => {
    return num1 / num2;
};
let pow = (num1) => {
    return num1 ** 2;
};
export { add, sub, multip, divide, pow };
