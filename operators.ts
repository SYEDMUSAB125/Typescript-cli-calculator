let add = (num1:number,num2:number):number=>{
 return num1+num2
};
let sub =(num1:number,num2:number):number=>{
    return num1-num2;
}
let multip =(num1:number,num2:number):number=>{
    return num1*num2;
}
let divide =(num1:number,num2:number):number=>{
    return num1/num2;
}
let pow =(num1:number):number=>{
    return num1**2;
}
export{add, sub , multip, divide, pow}